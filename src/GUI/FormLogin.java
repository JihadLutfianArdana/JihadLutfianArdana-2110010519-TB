    private void jcShowpassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcShowpassActionPerformed
        if(jcShowpass.isSelected()){
            tfPassword.setEchoChar((char)0);
        }else{
            tfPassword.setEchoChar('*');
        }// Menampilkan dan menyembunyikan password
    }//GEN-LAST:event_jcShowpassActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if("admin".equals(tfUsername.getText())){
           if("admin".equals(tfPassword.getText())){
               FormMenu Formberanda = new FormMenu();
               Formberanda.setVisible(true);
               dispose();
           }else{
               JOptionPane.showMessageDialog(rootPane, "Password Anda Belum Benar",
                       "Password Salah", JOptionPane.ERROR_MESSAGE);
            }
           }else{
                JOptionPane.showMessageDialog(rootPane, "Username Anda Belum Benar",
                       "Username Salah", JOptionPane.ERROR_MESSAGE);    
        }
    }//GEN-LAST:event_btnLoginActionPerformed

